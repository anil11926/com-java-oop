package com.java.oop.abstraction;

public class Customer {
    int id;
    String name;
    String email;
    String aadhaarNo;
    long phoneNo;

    public Customer(int id, String name, String email, String aadhaarNo, long phoneNo) {
        this.id = id;
        this.name = name;
        this.email = email;
        this.aadhaarNo = aadhaarNo;
        this.phoneNo = phoneNo;
    }
}
